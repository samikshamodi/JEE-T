"""## BM25 Model"""

def rerank(query,topk, tokenized_corpus, df):
  #preprocess the query
  tokenized_query = preprocess(query)
  print(tokenized_query)

  #k1 and b are parameters for our bm25 model
  k1 = 0.75
  b = 0.05

  bm25 = BM25Okapi(corpus = tokenized_corpus, k1=k1,b=b)
  
  #get the score for each document
  doc_scores = bm25.get_scores(tokenized_query)
  #print(doc_scores)

  #add the score of each document in the dataframe and sort in desceding order. Greater the score, greater the relevance
  tempdf = df.copy()
  tempdf['bm25score'] = doc_scores
  tempdf = tempdf.sort_values(by ='bm25score', ascending = 0)
  return tempdf.head(topk)

