#The pos tags given from the pos_tag function in preprocess were simplified by making our own pos_tagger function. 
def pos_tagger(nltk_tag):
	if nltk_tag.startswith('J'):
		return wordnet.ADJ
	elif nltk_tag.startswith('N'):
		return wordnet.NOUN
	elif nltk_tag.startswith('R'):
		return wordnet.ADV
	elif nltk_tag.startswith('V'):
		return wordnet.VERB
	else:		
		return None

#Preprocessing taken from assignment 1
def preprocess(textfile):
  textfile = textfile.lower() #convert to lowercase

  string_encode = textfile.encode("ascii", "ignore")  #removing unicode characters
  textfile = string_encode.decode()
  
  translator = str.maketrans('', '', string.punctuation) #remove punctuation
  textfile = textfile.translate(translator)

  textfile = " ".join(textfile.split()) #remove whitespace

  word_tokens = word_tokenize(textfile) #tokenize

  stop_words = set(stopwords.words("english")) #remove stopwords
  textfile = [word for word in word_tokens if word not in stop_words]

  lemmatizer = WordNetLemmatizer() # lemmatization
  lemmatized_sentence = []
  pos_tagged = nltk.pos_tag(textfile)
  #print(pos_tagged)
  wordnet_tagged = list(map(lambda x: (x[0], pos_tagger(x[1])), pos_tagged))
  #print(wordnet_tagged)
  for word, tag in wordnet_tagged:
    if tag is None:
      lemmatized_sentence.append(word) # if no available tag then append the token as is
    else:	
      lemmatized_sentence.append(lemmatizer.lemmatize(word, tag)) # else lemmatize the token using the tag
  textfile=lemmatized_sentence

  return textfile
